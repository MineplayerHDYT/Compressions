//==========================================================================================

    package com.saftno.compressions;

//==========================================================================================

    import net.minecraftforge.common.config.Configuration;
    import net.minecraftforge.fml.common.event.FMLPreInitializationEvent;

//==========================================================================================

    import java.io.File;

//==========================================================================================

    class Configurations {

    //======================================================================================

        private static String        type;
        private static Configuration file;

    //======================================================================================

        static class Initialization {

        //==================================================================================

            static void Pre( FMLPreInitializationEvent event ) {
            //------------------------------------------------------------------------------

                type = Configuration.CATEGORY_GENERAL;

            //------------------------------------------------------------------------------

                file = new Configuration(new File(Base.root + "/config/compressions.cfg"));

            //------------------------------------------------------------------------------
            }

        //==================================================================================

        }

    //======================================================================================

        static int      getDepth() {
        //----------------------------------------------------------------------------------
            file.load();
        //----------------------------------------------------------------------------------

            String varName = "Depth";
            String comment = "Levels of compression";

        //----------------------------------------------------------------------------------

            int min = 0;
            int def = 3;
            int max = 8;

        //----------------------------------------------------------------------------------

            int data = file.getInt( varName , type , def , min , max , comment );

        //----------------------------------------------------------------------------------
            file.save();
        //----------------------------------------------------------------------------------

            return data;

        //----------------------------------------------------------------------------------
        }

        static String[] getIDs() {
        //----------------------------------------------------------------------------------
            file.load();
        //----------------------------------------------------------------------------------

            String varName = "IDs";
            String comment = "Use ids like 'minecraft:sapling:3'";

        //----------------------------------------------------------------------------------

            String[] data = file.getStringList( varName , type , new String[]{} , comment );

        //----------------------------------------------------------------------------------
            file.save();
        //----------------------------------------------------------------------------------

            return data;

        //----------------------------------------------------------------------------------
        }

    //======================================================================================

    }

//==========================================================================================

