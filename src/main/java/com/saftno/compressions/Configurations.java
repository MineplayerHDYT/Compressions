//==========================================================================================

    package com.saftno.compressions;

//==========================================================================================

    import net.minecraftforge.common.config.Configuration;
    import net.minecraftforge.fml.common.event.FMLPreInitializationEvent;
    import org.jetbrains.annotations.NotNull;

//==========================================================================================

    import java.io.File;

//==========================================================================================

    @SuppressWarnings("WeakerAccess") class Configurations {

    //======================================================================================

        static String        type;
        static Configuration file;

    //======================================================================================

        static class Initialization {

        //==================================================================================

            static void Pre( @SuppressWarnings("unused") FMLPreInitializationEvent event ) {
            //------------------------------------------------------------------------------

                type = Configuration.CATEGORY_GENERAL;

            //------------------------------------------------------------------------------

                file = new Configuration(new File(Base.root + "/config/compressions.cfg"));

            //------------------------------------------------------------------------------
            }

        //==================================================================================

        }

    //======================================================================================

        static int getDepth() {
        //----------------------------------------------------------------------------------
            file.load();
        //----------------------------------------------------------------------------------

            String varName = "Depth";
            String comment = "Levels of compression";

        //----------------------------------------------------------------------------------

            int min = 0;
            int def = 3;
            int max = 8;

        //----------------------------------------------------------------------------------

            int data = file.getInt( varName , type , def , min , max , comment );

        //----------------------------------------------------------------------------------
            file.save();
        //----------------------------------------------------------------------------------

            return data;

        //----------------------------------------------------------------------------------
        }

    //======================================================================================

        @NotNull static String[] getIDs() {
        //----------------------------------------------------------------------------------
            file.load();
        //----------------------------------------------------------------------------------

            String varName = "List of items to have compression. 'minecraft:stone:2' "
                           + "adds a single stone variant, 'minecraft:stone' adds all "
                           + "stone varinats";
            String comment = "Add ids of items to have compression";

        //----------------------------------------------------------------------------------

            String[] data = file.getStringList( varName , type , new String[]{} , comment );

        //----------------------------------------------------------------------------------
            file.save();
        //----------------------------------------------------------------------------------

            return data;

        //----------------------------------------------------------------------------------
        }

    //======================================================================================

    }

//==========================================================================================

